package edu.raimundo.metodos;

public class Metodos {
    /*
     * Todo método deve ser nomeado como verbo;
     * Seguir padrão camelCase (Todas as letras minúsculas exceto a primeira letra da segunda palavra);
     * Cada método deve representar uma única responsabilidade.
    */

    public double somar(int numero1, int numero2){
        return numero1 + numero2;
    }

    public void imprimir(String texto){
        System.out.println(texto);
    }
}
